#!/usr/bin/env python3
"""
This module defines a simple mathematical operation.
It includes a function to add two float numbers.
"""

def add(a: float, b: float) -> float:
    """
    Adds two floating-point numbers and returns the result.

    Parameters:
    a (float): The first number to be added.
    b (float): The second number to be added.

    Returns:
    float: The sum of the input numbers.
    """
    return a + b
